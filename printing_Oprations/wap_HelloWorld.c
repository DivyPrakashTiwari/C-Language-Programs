// Include the standard input/output library
// This library contains the functions used for printing output, such as printf()
#include<stdio.h>

// Define the main function, which is the entry point of the program
// The main function is where the program's execution begins
// The int keyword indicates that the function returns an integer value
int main()
{
    // Print "HELLO WORLD" to the console
    // This is a call to the printf() function, which prints the string passed to it as an argument to the console
    // In this case, it prints "HELLO WORLD" followed by a newline character (\n)
    printf("HELLO WORLD !!\n");

    // Return 0 to indicate that the program has executed successfully
    // This line indicates that the function has executed successfully and returns 0 to the operating system
    return 0;
}